
public class Veterinaria {
	
	public void vacuna(Animal a) {
		System.out.println(a.nombre + ": ha sido vacunado");
	}
	
	public void examen(Animal a) {
		System.out.println(a.nombre + ": se le ha realizado el examen fisico");
	}
	
	public void dieta(Animal a) {
		System.out.println(a.nombre + ": se le ha asignado una beca");
	}
	
	public void muestra(Animal a) {
		System.out.println(a.nombre + ": se le ha tomado una muestra de sangre");
	}
	
}
